#include "cub3D.h"

void	clear_smap(t_smap **sma)
{
	t_smap	*tsma;

	while (*sma)
	{
		tsma = (*sma)->nxt;
		free((*sma)->s);
		free(*sma);
		*sma = tsma;
	}
}
